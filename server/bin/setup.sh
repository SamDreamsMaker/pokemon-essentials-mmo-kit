#!/usr/bin/env bash
#==============================================================================
# PEMK dedicated server — ONE-TIME dev setup for WSL Debian (or plain Debian/
# Ubuntu). Idempotent: safe to re-run. It:
#   1. installs Ruby, PostgreSQL, and the build deps for the `pg` native gem;
#   2. creates a USER-LEVEL Postgres cluster in $HOME/pemk-pgdata on port 55432
#      (never touches the system-wide cluster on 5432 — no root DB, no conflict);
#   3. writes ~/pemk-env.sh (paths + DATABASE_URL used by every other script);
#   4. creates the `pemk` (dev) and `pemk_test` databases;
#   5. `bundle install`s the gems into $HOME/.pemk-bundle;
#   6. migrates the dev database to the latest schema.
#
# After this, use  bin/dev-server.sh  (or PlayMMO-server.bat from Windows) to run.
# The apt step needs sudo (you'll be prompted once); everything else is per-user.
#==============================================================================
set -euo pipefail

# ---- fixed dev-cluster settings (match ~/pemk-env.sh) -----------------------
PGPORT=55432
PGDATA="$HOME/pemk-pgdata"
BUNDLE_DIR="$HOME/.pemk-bundle"
ENV_FILE="$HOME/pemk-env.sh"
DB_DEV=pemk
DB_TEST=pemk_test
PGUSER="$(id -un)"                 # the cluster superuser = your Linux user
SERVER_DIR="$(cd "$(dirname "$0")/.." && pwd)"   # .../server

say() { printf '\033[1;36m==>\033[0m %s\n' "$*"; }

# ---- 1. packages ------------------------------------------------------------
if ! command -v ruby >/dev/null || ! ls /usr/lib/postgresql/*/bin/initdb >/dev/null 2>&1; then
  say "Installing Ruby + PostgreSQL + build deps (sudo)…"
  sudo apt-get update -y
  sudo apt-get install -y ruby-full build-essential libpq-dev postgresql postgresql-client
else
  say "Ruby + PostgreSQL already installed — skipping apt."
fi

# Bundler (per-user, so no sudo gems). --user-install lands in the PATH we export.
if ! command -v bundle >/dev/null 2>&1; then
  say "Installing bundler (per-user)…"
  gem install --user-install bundler
fi

# ---- 2. detect Postgres + Ruby versions -------------------------------------
PGVER="$(ls -1 /usr/lib/postgresql 2>/dev/null | sort -n | tail -1)"
[ -n "$PGVER" ] || { echo "ERROR: no /usr/lib/postgresql/<ver> found." >&2; exit 1; }
PGBIN="/usr/lib/postgresql/$PGVER/bin"
RUBYABI="$(ruby -e 'print RUBY_VERSION.split(".")[0,2].join(".")').0"   # e.g. 3.1.0 — the gem-dir version
GEMBIN="$HOME/.local/share/gem/ruby/$RUBYABI/bin"
say "Using PostgreSQL $PGVER at $PGBIN, Ruby gem bin $GEMBIN"

# ---- 3. write ~/pemk-env.sh -------------------------------------------------
say "Writing $ENV_FILE"
cat > "$ENV_FILE" <<ENV
# PEMK dev environment (generated by server/bin/setup.sh). Sourced by the dev
# scripts. Edit here if your Postgres version or ports ever change.
export PGBIN="$PGBIN"
export PGPORT=$PGPORT
export PGHOST=127.0.0.1
export DATABASE_URL="postgres://$PGUSER@127.0.0.1:$PGPORT/$DB_DEV"
export BUNDLE_PATH="$BUNDLE_DIR"
export PATH="$GEMBIN:$PGBIN:\$PATH"
ENV
# Load it for the rest of THIS run.
# shellcheck disable=SC1090
source "$ENV_FILE"
export PATH="$GEMBIN:$PGBIN:$PATH"

# ---- 4. the user-level cluster ---------------------------------------------
if [ ! -f "$PGDATA/PG_VERSION" ]; then
  say "Creating a user-level Postgres cluster at $PGDATA (trust auth, no root)…"
  "$PGBIN/initdb" -D "$PGDATA" -U "$PGUSER" --auth=trust --encoding=UTF8 >/dev/null
else
  say "Cluster already exists at $PGDATA — keeping it."
fi

if ! "$PGBIN/pg_isready" -h 127.0.0.1 -p "$PGPORT" >/dev/null 2>&1; then
  say "Starting the cluster on 127.0.0.1:$PGPORT…"
  "$PGBIN/pg_ctl" -D "$PGDATA" \
    -o "-p $PGPORT -k /tmp -c listen_addresses=127.0.0.1" \
    -l "$PGDATA/server.log" -w start
else
  say "Cluster already running."
fi

# ---- 5. databases -----------------------------------------------------------
for db in "$DB_DEV" "$DB_TEST"; do
  if "$PGBIN/psql" -h 127.0.0.1 -p "$PGPORT" -U "$PGUSER" -d postgres -tAc \
       "SELECT 1 FROM pg_database WHERE datname='$db';" | grep -q 1; then
    say "Database '$db' already exists."
  else
    say "Creating database '$db'…"
    "$PGBIN/createdb" -h 127.0.0.1 -p "$PGPORT" -U "$PGUSER" "$db"
  fi
done

# ---- 6. gems + migrate ------------------------------------------------------
say "Installing gems into $BUNDLE_DIR…"
( cd "$SERVER_DIR" && BUNDLE_PATH="$BUNDLE_DIR" bundle install )

say "Migrating '$DB_DEV' to the latest schema…"
( cd "$SERVER_DIR" && DATABASE_URL="postgres://$PGUSER@127.0.0.1:$PGPORT/$DB_DEV" \
    BUNDLE_PATH="$BUNDLE_DIR" bundle exec rake db:migrate )

say "Done. Start the server with:  bash bin/dev-server.sh   (or PlayMMO-server.bat)"
say "Run the tests with:           bash bin/run-tests.sh"
